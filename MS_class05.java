//CHECKING IF AN INTEGER IS ODD OR EVEN

package com.company;

public class MS_class05 {
    public static void main(String[] args) {
        int x = 5;
        if (((x/2)*2)==x){
            System.out.println("even");
        }else{
            System.out.println("odd");
        }

        int y = 4;
        if (((y/2)*2)==y){
            System.out.println("even");
        }else{
            System.out.println("odd");
        }
    }
}
